// Player sprite with array of images and x & y coordinates
class Player {
	constructor(images, x_start, y_start){
		this.images = images;
		this.x_start = x_start;
		this.y_start = y_start;
	}
}

// Falling candy corns that player tries to catch
class Candy {
	constructor(image, x, y){
		this.image = image
		this.x = x
		this.y = y
	}
}

